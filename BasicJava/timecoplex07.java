import java.util.Scanner;

//Time complexity ==== O(n^2)

public class timecoplex07 {
    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);
        int n= sc.nextInt();
        for(int i =0;i<n;i++){
            for(int j=0;j<n;j++){
                System.out.println("hello!");
            }
        }
    }
}
